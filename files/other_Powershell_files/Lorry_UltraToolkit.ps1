# --- FILE MAKER ---
function Create-Files {
    Show-Title "File Generator"

    $path = "$env:USERPROFILE\Desktop\LorryFiles"
    if (!(Test-Path $path)) { New-Item -ItemType Directory -Path $path | Out-Null }

    for ($i = 1; $i -le 25; $i++) {
        $filename = "$path\File_$i.txt"
        Set-Content -Path $filename -Value "Generated by Lorry Ultra Toolkit (#$i)"
        Write-Host "Created: $filename" -ForegroundColor Green
        Start-Sleep -Milliseconds 80
    }

    Write-Host "`nüìÅ Files created on Desktop\LorryFiles!" -ForegroundColor Cyan
    Pause
}

